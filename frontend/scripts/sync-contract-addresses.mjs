import { mkdir, readdir, readFile, writeFile } from 'node:fs/promises'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __filename = fileURLToPath(import.meta.url)
const __dirname = path.dirname(__filename)
const frontendRoot = path.resolve(__dirname, '..')
const deploymentsRoot = path.resolve(frontendRoot, '..', 'contracts', 'ignition', 'deployments')
const outputPath = path.resolve(frontendRoot, 'src', 'config', 'contracts.generated.ts')

const futureIdToContractName = {
  'FluxCoreModule#FluxBuybackExecutor': 'FluxBuybackExecutor',
  'FluxCoreModule#FluxMultiPoolManager': 'FluxMultiPoolManager',
  'FluxCoreModule#mockUsdc': 'MockUSDC',
  'FluxCoreModule#mockUsdt': 'MockUSDT',
  'FluxCoreModule#mockWbtc': 'MockWBTC',
  'FluxCoreModule#FluxPoolFactory': 'FluxPoolFactory',
  'FluxCoreModule#FluxRevenueDistributor': 'FluxRevenueDistributor',
  'FluxCoreModule#FluxSwapFactory': 'FluxSwapFactory',
  'FluxCoreModule#FluxSwapRouter': 'FluxSwapRouter',
  'FluxCoreModule#FluxSwapTreasury': 'FluxSwapTreasury',
  'FluxCoreModule#FluxToken': 'FluxToken',
  'FluxCoreModule#MockWETH': 'MockWETH',
} 

const contractNames = [
  'FluxBuybackExecutor',
  'FluxMultiPoolManager',
  'MockUSDC',
  'MockUSDT',
  'MockWBTC',
  'FluxPoolFactory',
  'FluxRevenueDistributor',
  'FluxSwapFactory',
  'FluxSwapRouter',
  'FluxSwapTreasury',
  'FluxToken',
  'MockWETH',
]

async function readDeploymentDirectories(root) {
  try {
    const entries = await readdir(root, { withFileTypes: true })
    return entries
      .filter((entry) => entry.isDirectory() && /^chain-\d+$/.test(entry.name))
      .map((entry) => entry.name)
      .sort((left, right) => Number(left.slice(6)) - Number(right.slice(6)))
  } catch (error) {
    if (error && typeof error === 'object' && 'code' in error && error.code === 'ENOENT') {
      return []
    }

    throw error
  }
}

async function buildContractsByChain() {
  const deploymentDirs = await readDeploymentDirectories(deploymentsRoot)
  const result = {}

  for (const deploymentDir of deploymentDirs) {
    const chainId = Number(deploymentDir.slice(6))
    const deployedAddressesPath = path.join(deploymentsRoot, deploymentDir, 'deployed_addresses.json')
    const raw = await readFile(deployedAddressesPath, 'utf8')
    const deployedAddresses = JSON.parse(raw)
    const chainContracts = {}

    for (const [futureId, contractName] of Object.entries(futureIdToContractName)) {
      const address = deployedAddresses[futureId]
      if (typeof address === 'string' && address.startsWith('0x')) {
        chainContracts[contractName] = address
      }
    }

    result[chainId] = chainContracts
  }

  return result
}

function renderGeneratedFile(contractsByChain) {
  const chainLines = Object.entries(contractsByChain).map(([chainId, contracts]) => {
    const contractLines = contractNames
      .filter((contractName) => contracts[contractName])
      .map(
        (contractName) =>
          `    ${contractName}: '${contracts[contractName]}' as Address,`,
      )
      .join('\n')

    return `  ${chainId}: {\n${contractLines}\n  },`
  })

  return `import type { Address } from 'viem'

// This file is auto-generated by \`npm run contracts:sync\`.
// Do not edit it manually.

export const fluxContractNames = ${JSON.stringify(contractNames)} as const

export type FluxContractName = (typeof fluxContractNames)[number]
export type FluxContractAddressMap = Partial<Record<FluxContractName, Address>>

export const generatedContractAddressesByChain = {
${chainLines.join('\n')}
} as const satisfies Record<number, FluxContractAddressMap>
`
}

async function main() {
  const contractsByChain = await buildContractsByChain()
  const content = renderGeneratedFile(contractsByChain)

  await mkdir(path.dirname(outputPath), { recursive: true })
  await writeFile(outputPath, content, 'utf8')

  console.log(`Synced contract addresses to ${path.relative(frontendRoot, outputPath)}`)
}

await main()
